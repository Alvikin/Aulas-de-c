#include <stdio.h>
//Aula de Utilização Variáveis

int main (void) { // int main vazio: () ou void: (void) é a mesma coisa

    int minhaidade;
    minhaidade = 23;

    int maeidade;
    maeidade = 48;
   
    // pode-se definir o valor da variavel int na mesma linha ou não

    int paiidade = 49;


    printf ("minha idade = %i\npai idade = %i\nmae idade = %i", 
    minhaidade, paiidade, maeidade);

    return 0;
}