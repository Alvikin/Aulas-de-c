#include <stdio.h>
// Aula de Fazer Comentários em C | Caractere Nova Linha \n

int main (void) { // int main vazio: () ou void: (void) é a mesma coisa

    //Fazendo um comentario em 1 linha

    /*
     Fazendo comentario
     em varias
     linhas
    */
    printf ("Arroz Branco. \n");

    // \n pula linhas
    // se coloca ele no final para pular a última linha do progama
    // Sem o \n no primeiro printf, o progama vai printar Arroz Branco. Arroz Branco.

    printf ("Arroz Branco. \n");

    return 0;
}